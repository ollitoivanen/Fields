package com.fields.curiumx.fields;

public class MessageMap {
    private String user1;
    private String user2;

    public MessageMap(){

    }

    public MessageMap(String user1, String user2){
        this.user1 = user1;
        this.user2 = user2;
    }

    public String getUser1() {
        return user1;
    }

    public String getUser2() {
        return user2;
    }
}
